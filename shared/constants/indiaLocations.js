export function getStates() {
  return INDIA_LOCATIONS.map(({ code, nameEn, nameHi }) => ({ code, nameEn, nameHi }))
}

export function getDistricts(stateCode) {
  const state = INDIA_LOCATIONS.find((s) => s.code === stateCode)
  if (!state) return []
  return state.districts.map(({ code, nameEn, nameHi }) => ({ code, nameEn, nameHi }))
}

export function getCities(stateCode, districtCode) {
  const state = INDIA_LOCATIONS.find((s) => s.code === stateCode)
  if (!state) return []
  const district = state.districts.find((d) => d.code === districtCode)
  if (!district) return []
  return district.cities.map(({ code, nameEn, nameHi }) => ({ code, nameEn, nameHi }))
}
